namespace TemplateHOG.templates {
    partial class custom {
        string project;
        string progress;
        string path;
        string room;
        string zz;
        string prg;
        public custom(string project, string progress, string path,
            string room, string zz, string prg) {
            this.project = project;
            this.progress = progress;
            this.path = path;
            this.room = room;
            this.zz = zz;
            this.prg = prg;
        }

    }
}